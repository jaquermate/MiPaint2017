    private void inicializaBuffers(){
        buffer= (BufferedImage)lienzo.createImage(lienzo.getWidth(),lienzo.getHeight());//creamos un lienzo del mismo tama単o
        Graphics2D g2=buffer.createGraphics();//creamos una imagen modificable
        g2.setColor(Color.white);//dibujamos un rectangulo blanco del tama単o del lienzo
        g2.fillRect(0, 0, buffer.getWidth(), buffer.getHeight());
        //inicializo el buffer2
        buffer2= (BufferedImage)lienzo.createImage(lienzo.getWidth(),lienzo.getHeight());//creamos un lienzo del mismo tama単o
        g2=buffer2.createGraphics();//creamos una imagen modificable
        g2.setColor(Color.white);//dibujamos un rectangulo blanco del tama単o del lienzo
        g2.fillRect(0, 0, buffer.getWidth(), buffer.getHeight());
        
    }
